namespace Core\View;

/**
 * Class AjaxView
 *
 * @package Core\View
 */
class AjaxView extends AppView
{

    /**
     * The name of the layout file.
     *
     * @var string
     */
    public $layout = 'ajax';

    /**
     * Initialization hook method.
     *
     * @return void
     */
    public function initialize()
    {
        parent::initialize();
        $this->response->type('ajax');
    }
}
